class Dvdplayer
{
        public void on()
        {
                System.out.println("DVD Player on!");
        }
        public void off()
        {
                System.out.println("DVD Player off!");
        }
        public void play()
        {
                System.out.println("DVD Play!");
        }
        public void pause()
        {
                System.out.println("DVD Pause!");
        }
        public void setvol(int lev){
                System.out.println("dvd vol set to " + lev);
        }
}
